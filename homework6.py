# Работа со словарями
my_dict = {"Алексей": 1990,"Мария": 1985,"Иван": 2000 }

# Выводим словарь
print("Словарь my_dict:", my_dict)

# Выводим значение по существующему ключу
print("Год рождения Алексея:", my_dict.get("Алексей"))

# Выводим значение по отсутствующему ключу без ошибки
print("Год рождения Петра:", my_dict.get("Петр", "Ключ не найден"))

# Добавляем две новые пары
my_dict["Ольга"] = 1992
my_dict["Сергей"] = 1988

# Удаляем одну из пар по существующему ключу и выводим значение
removed_value = my_dict.pop("Мария", None)
print("Удаленный год рождения Марии:", removed_value)

# Выводим измененный словарь
print("Измененный словарь my_dict:", my_dict)

# Работа с множествами
my_set = {1, 2, 3, "hello", "world", 1, 2}

# Выводим множество
print("Множество my_set:", my_set)

# Добавляем два новых элемента
my_set.add(4)
my_set.add("python")

# Удаляем один любой элемент
my_set.pop()

# Выводим измененное множество
print("Измененное множество my_set:", my_set)